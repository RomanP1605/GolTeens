P = 1000  # початкова сума грошей
r = 0.25  # річний відсоток (25%)
n = 1  # кількість разів, коли відсотки нараховуються за рік
t = int(input("Введіть кількість років: "))  # кількість років депозиту, яку вводить користувач

A = P * (1 + r/n) ** (n*t)  # розрахунок кінцевої суми грошей

print(f"Після {t} років на депозиті буде {A:.2f} грн")
